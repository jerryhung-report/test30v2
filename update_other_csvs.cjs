function updateCsv(filePath) {
  if (!fs.existsSync(filePath)) return;
  
  let content = fs.readFileSync(filePath, 'utf8');
  let updatedCount = 0;
  
  const lines = content.split('\n');
  for (let i = 0; i < lines.length; i++) {
    for (const [code, newName] of Object.entries(fundMapping)) {
      if (lines[i].includes(code)) {
        if (filePath.includes('funds_export.csv')) {
          const parts = lines[i].split(',');
          if (parts[0] === code) {
            parts[1] = `"${newName}"`;
            lines[i] = parts.join(',');
            updatedCount++;
          }
        }
        else if (filePath.includes('fund_mapping.csv')) {
          const parts = lines[i].split(',');
          if (parts[2] === code) {
            parts[3] = newName;
            lines[i] = parts.join(',');
            updatedCount++;
          }
        }
      }
    }
  }
  
  if (updatedCount > 0) {
    fs.writeFileSync(filePath, lines.join('\n'));
    console.log(`Updated ${updatedCount} lines in ${filePath}`);
  }
}

